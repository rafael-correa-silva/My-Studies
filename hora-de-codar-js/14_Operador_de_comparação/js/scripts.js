var numero = '5';

if (numero === 5) {
    console.log("O número é igual a 5."); // O operador de comparação === verifica se o número é estritamente igual a 5, ou seja, verifica tanto o valor quanto o tipo.
}

if(numero !== 5) {
    console.log("Não é o número 5 do tipo number."); // O operador de comparação !== verifica se o número é estritamente diferente de 5, ou seja, verifica tanto o valor quanto o tipo.
}