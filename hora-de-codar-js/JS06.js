var numero = 5;

console.log(numero);
console.log(typeof numero);

var float = 5.5;

console.log(float);
console.log(typeof float);

var textoComNumero = '523'; // String com número, se eu colocar aspas, ele vira string, se não colocar aspas, ele vira número

console.log(textoComNumero);
console.log(typeof textoComNumero);

console.log("NaN");
console.log(typeof NaN); // NaN é um tipo de número, mas representa um valor que não é um número válido.

console.log(typeof +Infinity); // Infinity é um valor numérico que representa infinito, e também é do tipo número.

console.log(typeof -Infinity); // -Infinity é um valor numérico que representa infinito negativo, e também é do tipo número.