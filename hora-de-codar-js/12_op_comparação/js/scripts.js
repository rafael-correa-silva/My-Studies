var idade = 17;

if (idade >= 18) { // O operador de comparação >= verifica se a idade é maior ou igual a 18
    console.log("Você pode tirar habilitação.");
}

if (idade <= 17) { // O operador de comparação <= verifica se a idade é menor ou igual a 17, pode usar somente o < nesse caso porém teria que ser 18 e não 17
    console.log("Você não pode tirar habilitação.");
}     

var nome = "Rafael";

if (nome == "Rafael") { // O operador de comparação == verifica se o nome é igual a "Rafael"
    console.log("Olá, Rafael!");
}

if (nome != "Rafael") { // O operador de comparação != verifica se o nome é diferente de "Rafael"
    console.log("Olá, você não é Rafael!");
}

if(20 > 10) { // O operador de comparação > verifica se 20 é maior que 10
    console.log("20 é maior que 10.");
}

if(100 < 200) { // O operador de comparação < verifica se 100 é menor que 200
    console.log("100 é menor que 200.");
}
